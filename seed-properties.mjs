async function seedProperties() {
  const connection = await pool.getConnection();
  try {
    console.log('Seeding properties...');
    for (const prop of sampleProperties) {
      await connection.query(
        'INSERT INTO properties (titleEs, titleEn, titleFr, titleDe, titleAr, descriptionEs, descriptionEn, descriptionFr, descriptionDe, descriptionAr, location, price, type, propertyType, bedrooms, bathrooms, squareMeters, yearBuilt, imageUrl, featured, active) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)',
        [
          prop.titleEs,
          prop.titleEn,
          prop.titleFr,
          prop.titleDe,
          prop.titleAr,
          prop.descriptionEs,
          prop.descriptionEn,
          prop.descriptionFr,
          prop.descriptionDe,
          prop.descriptionAr,
          prop.location,
          prop.price,
          prop.type,
          prop.propertyType,
          prop.bedrooms,
          prop.bathrooms,
          prop.squareMeters,
          prop.yearBuilt,
          prop.imageUrl,
          true,
          true,
        ]
      );
    }
    console.log('✓ Successfully seeded 10 properties');
    process.exit(0);
  } catch (error) {
    console.error('Error seeding properties:', error);
    process.exit(1);
  } finally {
    await connection.release();
    await pool.end();
  }
}

seedProperties();
